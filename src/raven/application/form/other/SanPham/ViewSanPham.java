    private SanPham getForm(){
        String ma = txtMa.getText();
        String ten = txtTen.getText();
        Integer soLuong = Integer.valueOf(txtSoLuong.getText());
        String moTa = txtMoTa.getText();
        SanPham sp = new SanPham(ma, ten, soLuong, moTa);
        return sp;
    }

