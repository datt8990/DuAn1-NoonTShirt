    public void showData(ArrayList<SanPham> arSP){
        dtm.setRowCount(0);
        AtomicInteger index = new AtomicInteger(1);
        for (SanPham sp : arSP) {
            dtm.addRow(new Object[]{
                sp.getId(),sp.getMa_san_pham(),sp.getTen_san_pham(),sp.getSo_luong(),sp.getMo_ta()
            });
        }
    }
    
    public void detailSP(int index){
        SanPham sp = dsSP.get(index);
        txtMa.setText(sp.getMa_san_pham());
        txtTen.setText(sp.getTen_san_pham());
        txtSoLuong.setText(sp.getSo_luong()+"");
        txtMoTa.setText(sp.getMo_ta());
    }
    
